package com.yx.dao;

public class Roomtype {

	// 房型ID
	private int id;
	
	// 房型
	private String roomType;

	// 取得房型ID
	public int getId() {
		return id;
	}

	// 设置房型ID
	public void setId(int id) {
		this.id = id;
	}

	// 取得房型
	public String getRoomType() {
		return roomType;
	}

	// 设置房型
	public void setRoomType(String roomType) {
		this.roomType = roomType;
	}
}
